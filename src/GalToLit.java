public class GalToLit {
    // convertation gallons ito liters
    public static void main(String args[]){
        double gallons; // variable gallons
        double liters; // variable liters
        gallons = 10; // number of gallons
        liters = gallons * 3.7854; // convertation
        System.out.println(gallons + " gallons is " + liters + " liters");
    }
}
